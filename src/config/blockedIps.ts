// --- IPv4 helpers (Edge-runtime safe: pure JS, no Node APIs) -----------------

// Parse a dotted-quad IPv4 string into an unsigned 32-bit integer, or null if
// it is not a valid IPv4 address.
function ipv4ToInt(ip: string): number | null {
  const parts = ip.split(".");
  if (parts.length !== 4) return null;
  let value = 0;
  for (const part of parts) {
    // Reject empty, non-numeric, or out-of-range octets (and leading "+"/spaces).
    if (!/^\d{1,3}$/.test(part)) return null;
    const octet = Number(part);
    if (octet > 255) return null;
    value = value * 256 + octet;
  }
  return value >>> 0;
}

interface Cidr {
  base: number; // network address as uint32
  mask: number; // netmask as uint32
}

// Parse a "a.b.c.d/prefix" string into a {base, mask}, or null if invalid.
function parseCidr(entry: string): Cidr | null {
  const [addr, prefixStr] = entry.split("/");
  const ip = ipv4ToInt(addr);
  if (ip === null) return null;
  if (!/^\d{1,2}$/.test(prefixStr)) return null;
  const prefix = Number(prefixStr);
  if (prefix > 32) return null;
  // prefix 0 → mask 0 (shifting a 32-bit value by 32 is undefined in JS).
  const mask = prefix === 0 ? 0 : (~0 << (32 - prefix)) >>> 0;
  return { base: (ip & mask) >>> 0, mask };
}

interface IpRange {
  from: number; // first address as uint32 (inclusive)
  to: number; // last address as uint32 (inclusive)
}

// Parse a "<from> - <to>" string into a {from, to}, or null if invalid.
// Whitespace around the dash is optional: "a-b" and "a - b" both work.
function parseRange(entry: string): IpRange | null {
  const dash = entry.indexOf("-");
  if (dash === -1) return null;
  const from = ipv4ToInt(entry.slice(0, dash).trim());
  const to = ipv4ToInt(entry.slice(dash + 1).trim());
  if (from === null || to === null) return null;
  // Tolerate a reversed range (to < from) by normalising.
  return from <= to ? { from, to } : { from: to, to: from };
}

// --- Build the block lists once at module load -------------------------------

const entries = BLOCKED_IPS_RAW.split("\n")
  .map((line) => line.trim())
  .filter((line) => line.length > 0 && !line.startsWith("#"));

const exactIps = new Set<string>();
const cidrRanges: Cidr[] = [];
const ipRanges: IpRange[] = [];

for (const entry of entries) {
  if (entry.includes("/")) {
    const cidr = parseCidr(entry);
    if (cidr) cidrRanges.push(cidr);
  } else if (entry.includes("-")) {
    const range = parseRange(entry);
    if (range) ipRanges.push(range);
  } else {
    exactIps.add(entry);
  }
}

export const BLOCKED_IPS: ReadonlySet<string> = exactIps;
export const BLOCKED_CIDRS: ReadonlyArray<Cidr> = cidrRanges;
