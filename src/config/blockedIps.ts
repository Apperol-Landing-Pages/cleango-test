// Where blocked IPs are sent (the public landing). They cannot reach anything beyond it.
export const BLOCK_REDIRECT_PATH = "/";

export function isBlockedIp(ip: string | null): boolean {
  if (ip == null) return false;
  // Fast path: exact match (covers both IPv4 and IPv6 literals).
  if (exactIps.has(ip)) return true;
  // Subnet / range matching (IPv4 only).
  if (cidrRanges.length > 0 || ipRanges.length > 0) {
    const value = ipv4ToInt(ip);
    if (value !== null) {
      for (const { base, mask } of cidrRanges) {
        if (((value & mask) >>> 0) === base) return true;
      }
      for (const { from, to } of ipRanges) {
        if (value >= from && value <= to) return true;
      }
    }
  }
  return false;
}
