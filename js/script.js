const fallbackLang = "en";
const supportedLangs = Object.keys(translations);

const normalizeLanguage = (value) => {
  if (!value) return fallbackLang;
  const lowered = value.toLowerCase();

  if (lowered === "pt" || lowered.startsWith("pt-br")) return "pt-BR";
  if (lowered.startsWith("pt-pt")) return "pt-PT";
  if (lowered === "es-419" || lowered.startsWith("es-419")) return "es-419";
  if (lowered === "es-mx" || lowered === "es-ar" || lowered === "es-cl" || lowered === "es-co" || lowered === "es-pe") return "es-419";
  if (lowered.startsWith("es")) return "es";
  if (lowered.startsWith("fr")) return "fr";
  if (lowered.startsWith("da")) return "da";
  if (lowered.startsWith("ja")) return "ja";
  if (lowered.startsWith("fil") || lowered.startsWith("tl")) return "fil";
  if (lowered.startsWith("de")) return "de";
  if (lowered.startsWith("nb") || lowered.startsWith("no")) return "nb";
  if (lowered.startsWith("sv")) return "sv";
  if (lowered.startsWith("it")) return "it";
  if (lowered.startsWith("nl")) return "nl";
  if (lowered.startsWith("ro")) return "ro";
  if (lowered.startsWith("en")) return "en";

  return supportedLangs.includes(value) ? value : fallbackLang;
};

const applyTranslations = (lang) => {
  const dictionary = translations[lang] || translations[fallbackLang];
  document.documentElement.lang = lang;

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.dataset.i18n;
    if (dictionary[key]) {
      element.textContent = dictionary[key];
    }
  });

  document.querySelectorAll("[data-i18n-html]").forEach((element) => {
    const key = element.dataset.i18nHtml;
    if (dictionary[key]) {
      element.innerHTML = dictionary[key];
    }
  });

  document.title = dictionary.protectPhone || translations[fallbackLang].protectPhone;
};

const detectLanguage = () => {
  const params = new URLSearchParams(window.location.search);
  const queryLang = params.get("lang");
  if (queryLang) return normalizeLanguage(queryLang);

  const storedLang = window.localStorage.getItem("landing-lang");
  if (storedLang) return normalizeLanguage(storedLang);

  return normalizeLanguage(navigator.language || navigator.userLanguage);
};

const setLanguage = (lang) => {
  const normalized = normalizeLanguage(lang);
  window.localStorage.setItem("landing-lang", normalized);
  applyTranslations(normalized);
  return normalized;
};

window.APP_LANG = {
  getSupported: () => [...supportedLangs],
  setLang: setLanguage,
  getCurrent: () => document.documentElement.lang || fallbackLang,
};

applyTranslations(detectLanguage());
